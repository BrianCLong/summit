/**
 * Threat & Exposure Consumption Format (TECF) Definitions
 *
 * TECF is a normalized format for ingesting security events and exposures
 * into the Adaptive Tradecraft Graph.
 */

export interface TecfEvent {
  /** Unique ID of the event from the source */
  id: string;

  /** Type of event (e.g., 'saas_login', 'repo_access', 'vulnerability_found') */
  type: string;

  /** Timestamp of the event (ISO 8601). NOT used for evidence determinism. */
  timestamp: string;

  /** Source system identifier (e.g., 'okta', 'github', 'spycloud') */
  source: string;

  /** Raw payload data */
  payload: Record<string, unknown>;

  /**
   * Deterministic Evidence ID associated with this event.
   * Generated by hashing the content (excluding timestamp/volatile fields).
   */
  evidence_id?: string;
}

export interface TecfStream {
  source: string;
  events: TecfEvent[];
}
